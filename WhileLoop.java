public class WhileLoop{
    public static void main(String[] args) {
        // while (condition/boolean)
        int number= 6;
        while (number<=5) {
            System.out.println("While"+number);
            //increment /decrement here
            number++;
            
        }

        //do while
        int number2 = 6;
        do{
            System.out.println("Do while"+number2);
            number2++;
        } while(number2<=5);

    }
    
}
